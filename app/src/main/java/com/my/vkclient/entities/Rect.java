package com.my.vkclient.entities;

import java.io.Serializable;

public class Rect implements Serializable {
    private float x;
    private float y;
    private float x2;
    private float y2;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getX2() {
        return x2;
    }

    public float getY2() {
        return y2;
    }
}
